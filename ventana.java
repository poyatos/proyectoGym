    private void Buscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Buscar2ActionPerformed

        resultado = PagoBuscar.getText();

        if (jRadioId.isSelected()) {
            try {
                datos.mostrarAlumnosId(queryBuscarAlumnosID, resultado, FiltradoAlumnosPagos);
            } catch (ClassNotFoundException | SQLException ex) {
                txtError.setText("Error BBDD");
            }
        }

        if (jRadioApellido.isSelected()) {
            try {
                datos.mostrarAlumnosApellido(queryBuscarAlumnosApellido, resultado, FiltradoAlumnosPagos);
            } catch (ClassNotFoundException | SQLException ex) {
                txtError.setText("Error BBDD");
            }
        }


    }//GEN-LAST:event_Buscar2ActionPerformed

